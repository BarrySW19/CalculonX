package barrysw19.calculon.engine;

import org.junit.Test;

import static org.junit.Assert.assertEquals;

public class BitmapsTest {

    @Test
    public void confirmHorizontalValues() {
        assertEquals(0b00000000_00000000_00000000_00000000_00000000_00000000_00000000_01111110L, Bitmaps.SLIDE_MOVES[0][7]);
        assertEquals(0b00000000_00000000_00000000_00000000_00000000_00000000_00111100_00000000L, Bitmaps.SLIDE_MOVES[9][14]);
        assertEquals(0b00000000_00000000_00000000_00000000_00000000_00011000_00000000_00000000L, Bitmaps.SLIDE_MOVES[18][21]);
        assertEquals(0b00000000_00000000_00000000_00000000_00000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[27][28]);
    }

    @Test
    public void confirmVerticalValues() {
        assertEquals(0b00000000_00000001_00000001_00000001_00000001_00000001_00000001_00000000L, Bitmaps.SLIDE_MOVES[0][56]);
        assertEquals(0b00000000_00000010_00000010_00000010_00000010_00000010_00000010_00000000L, Bitmaps.SLIDE_MOVES[1][57]);
        assertEquals(0b00000000_00000000_00000010_00000010_00000010_00000010_00000000_00000000L, Bitmaps.SLIDE_MOVES[9][49]);
        assertEquals(0b00000000_00000000_00000000_00000010_00000010_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[17][41]);
        assertEquals(0b00000000_00000000_00000000_00000000_00000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[25][33]);
    }

    @Test
    public void confirmDiagonalValues() {
        assertEquals(0b00000000_01000000_00100000_00010000_00001000_00000100_00000010_00000000L, Bitmaps.SLIDE_MOVES[0][63]);
        assertEquals(0b00000000_00000010_00000100_00001000_00010000_00100000_01000000_00000000L, Bitmaps.SLIDE_MOVES[7][56]);

        assertEquals(0b00000000_00100000_00010000_00001000_00000100_00000010_00000000_00000000L, Bitmaps.SLIDE_MOVES[8][62]);
        assertEquals(0b00000000_00010000_00001000_00000100_00000010_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[16][61]);
        assertEquals(0b00000000_00001000_00000100_00000010_00000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[24][60]);
        assertEquals(0b00000000_00000100_00000010_00000000_00000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[32][59]);
        assertEquals(0b00000000_00000010_00000000_00000000_00000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[40][58]);

        assertEquals(0b00000000_00000000_00000000_00000010_00000100_00001000_00010000_00000000L, Bitmaps.SLIDE_MOVES[5][40]);
        assertEquals(0b00000000_00000000_00000000_00000000_00000000_00000010_00000100_00000000L, Bitmaps.SLIDE_MOVES[3][24]);
        assertEquals(0b00000000_00001000_00010000_00100000_01000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[23][58]);
        assertEquals(0b00000000_01000000_00000000_00000000_00000000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[47][61]);

        assertEquals(0b00000000_00000000_00000000_00001000_00010000_00000000_00000000_00000000L, Bitmaps.SLIDE_MOVES[21][42]);
    }
}